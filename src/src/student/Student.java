package student;

public class Student {
	private String sid;
	private String pid;
	private String sname;
	private int xdNumber;
	public String getSid() {
		return sid;
	}
	public void setSid(String sid) {
		this.sid = sid;
	}
	public String getPid() {
		return pid;
	}
	public void setPid(String pid) {
		this.pid = pid;
	}
	public String getSname() {
		return sname;
	}
	public void setSname(String sname) {
		this.sname = sname;
	}
	public int getXdNumber() {
		return xdNumber;
	}
	public void setXdNumber(int xdNumber) {
		this.xdNumber = xdNumber;
	}
	
}
